//Arithmetic operators
let x=45;
let y=15;
console.log("x + y =",x+y) //addition
console.log("x - y =",x-y) //substraction
console.log("x * y =",x*y) //multiplication
console.log("x / y =",x/y) //division
console.log("x % y =",x%y) //remainder
console.log("x**y =",x**2) //exponential

//Assignment operator
let z=45;
console.log(z+=5)//45+5=50
console.log(z-=5)//50-5=45
console.log(z*=5)//45x5=225
console.log(z/=5)//225/5=45

//comparison operator
let a=5;
let b=8;
console.log(a===b)
console.log(a!==b)
console.log(a<b)
console.log(a>b)
console.log(a>=b)
console.log(a<=b)
  
//logical operator
let p=5;
